function pokeParticleKo(name: string): string {
  if (!name) return '가';
  const code = name.charCodeAt(name.length - 1);
  if (code >= 0xac00 && code <= 0xd7a3) {
    return (code - 0xac00) % 28 === 0 ? '가' : '이';
  }
  return '가';
}

